package OrientacaoObjeto.Pilares.Encapsulamento;

public class Conceito {
	
	/*
	 * --- colocar algo dentro de um capsula(objeto) ---
	 * - Ex carro
	 * 		- volante esta publico, pedal está publico, mas o funcionamento do motor está privado
	 * 
	 * - Importante pois se tudo estiver visivel pode gerar uma complexidade entre os objetos
	 * 
	 * - Baixo nivel encapsulamento gera alto acoplamento
	 * 
	 * - Modificadores de acesso (public > protected > package > private)
	 * 		- Public
	 * 			- Atributo/metodo public qualquer classe do sistema pode acessar, sem nenhuma restrição
	 * 		
	 * 		- Protected
	 * 			- Atributo/metodo protected pode acessar o atributo de outro pacote desde que ele seja herdado
	 * 		
	 * 		- package(default)
	 * 			- Atributo/metodo package somente classes do mesmo pacote podem acessar
	 * 		
	 * 		- private
	 * 			- Atributo/metodo private só pode acessado pela propria classe
	 * 
	 */

}
