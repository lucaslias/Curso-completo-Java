package ArraysCollections.Collection;

public class Conceito {
	
	/*
	 * Tamanho variavel
	 * Dados misturados, porém boa pratica é não misturar diferentes tipos de dados
	 * Não suporta tipos primitivos, usar Wrappers
	 */
	
	/*
	 * Set
	 * - Não ordenado
	 * - Não indexado(não tem como acessar elemento pelo indexe) 
	 * - Não aceita repetição 
	 */
	
	/*
	 * List
	 * - Indexada
	 * - Aceita repetição
	 */
	
	/*
	 * Map
	 * - Chave valor
	 * - Chave não aceita repetição, valor aceita
	 */
	
	/*
	 * Queue
	 * - Implementa fila
	 * - FIFO (firt in, first out)
	 */
	
	/*
	 * Stack
	 * - Implementa pilha
	 * - LIFO (Last in, first out)
	 */

}
