package EstruturasDeControle;

public class EstruturaIf {
	
	public static void main(String[] args) {
		
		double media = 6.3;
		
		if(media >= 7) {
			System.out.println("aprovado");
		} else if(media == 6) {
			System.out.println("recuperação");
		} else {
			System.out.println("reprovado");
		}
		
	}

}
