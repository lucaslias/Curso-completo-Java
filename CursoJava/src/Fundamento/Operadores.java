package Fundamento;

public class Operadores {

	public static void main(String[] args) {
		
		//operadores unarios
			//a++ (a = a + 1)
		
		/* operadores binarios
			3 + 2
				-prefix ++a
				-posfix a++
				-infix a+a  
		 */
		
		//operadores ternarios
		// valor1 ? valor2 : valor3
		double media = 6.5;
		String resultado = media >= 7.0 ? "aprovado" : "reprovado";
		System.out.println(resultado);
		
		/*operadores logicos
			- && (e)
			- || (ou)
		*/
		
		/* Operadores relacionais
			- == (igual)
			- != (diferente)
			>= (maior igual)
			<= (menor igual)
		 */
		
		//Operadores de atribuição 
			// forma de atribuição: , *=, /=. %=
			int a = 1;
			int b = 2;
			a += b; // mesma coisa que a = a + b
	
	
		

		
	}
	
}
