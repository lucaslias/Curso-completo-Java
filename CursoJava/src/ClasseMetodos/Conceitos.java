package ClasseMetodos;

//Nome da classe mesmo nome do arquivo
public class Conceitos {
	
	/*
	 *  - Classe:  
	 *  	-Começar por letra maiuscula
	 *  	-Tem atributos(dados) e comportamentos(metodos)
	 *  	-Representa uma abstração Produto -> tem preço, quantidade, peso, valor, ...
	 *  	-Atributos constantes e Atributos variaveis 
	 *  	-Pode ter varios construtores
	 *  	-Pode ter varias classes dentro
	 */
	
	/*
	 *  - Objeto: 
	 *  	-Começar por letra maiuscula
	 *  	- Dados gerados pela/a partir da classe
	 *  	- Objeto == Instancia 	   
	 */
	
	/*
	 *  - Classe X Objeto: 
	 *  	- Classe é o molde, objetos são os "produtos" gerado
	 *  	- Classe Produto(nome, preço, desconto) -> "notebook, 5000reais, 15%", "celular, 3000reais, 5%"
	 *  	- Metodo construtor, cria os objetos	   
	 */

	/*
	 *  - Metodo: 
	 *  	- Pode receber ou não entradas, e podem ou não retornar algo
	 *  	- primeira letra minuscula
	 *  	- Tipo nomeMetodo(tipoParam param) { ... }
	 */
	
	/*
	 *  - Valor x Referencia : 
	 *  	- valor é para tipo primitivo, referencia é para objeto
	 *  	- referencia  "armazena" o endereço de memoria onde  estão os "valores"
	 *  	- valor "armazena" o valor direto
	 *  	
	 *  	double a = 1;
	 *  	double b = a;  //atribuição por valor
	 *  
	 *  	Data d1  = new Data(1,2,2000)
	 *  	Data d2 = d1  //atribuição por referencia se altera em um muda nos dois
	 */
	
	/*
	 *  - This e This(): 
	 *  	- this = independente de conflito de nomes, usado para acessar a variavel de instancia 
	 *  	- this() = a partir de um construtor chamar outro
	 */
	
	/*
	 *  - Variavel Local, só acessa dentro do metodo, precisa instanciar (não é inicializa, variavel de instancia é inicializada com valor padrão)
	 *  - não pode acessar acessar atributos ou comportamentos de variaveis nula
	 */
	
}
